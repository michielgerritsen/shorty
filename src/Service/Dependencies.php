namespace ControlAltDelete\Shorty\Service;

class Dependencies
{
    public function check()
    {
        $path = $_SERVER['PATH'];
        $parts = array_filter(explode(':', $path), function ($part) {
            return strpos($part, '.composer/vendor/bin') !== false;
        });

        if (empty($parts)) {
            return false;
        }

        return true;
    }
}